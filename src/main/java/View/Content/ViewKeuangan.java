    private void refreshTabel() {
        DefaultTableModel model = (DefaultTableModel) tabelKeuangan.getModel();
        model.setRowCount(0); //

        ArrayList<Model.ModelTransaksi> list = control.ambilSemuaModelTransaksi();

        if (list != null) {
            for (Model.ModelTransaksi t : list) {
                Object[] row = {
                    t.idTransaksi,
                    t.tanggal,
                    t.jenis,
                    t.kategori,
                    t.nominal,
                    t.keterangan
                };
                model.addRow(row);
            }
        }
    }

