    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed

        if (idTerpilih == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data pada tabel yang ingin diubah terlebih dahulu!");
            return;
        }

        try {
            String jenis = cbJenis_Transaksi.getSelectedItem().toString();
            String kat = cbKategori.getSelectedItem().toString();
            String ket = txtKeterangan.getText().trim();

            String teksNominal = txtNominal.getText().replaceAll("\\D", "");
            
            if (teksNominal.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nominal tidak boleh kosong!");
                return;
            }

            double nom = Double.parseDouble(teksNominal);

            if (nom <= 0) {
                JOptionPane.showMessageDialog(this, "Nominal harus lebih dari 0!");
                return;
            }

            boolean sukses = control.ubahTransaksi(idTerpilih, jenis, kat, nom, ket);

            if (sukses) {
                JOptionPane.showMessageDialog(this, "Data transaksi berhasil diperbarui!");
                refreshTabel();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal memperbarui data.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

        if (txtNominal.getText().trim().isEmpty() || txtKeterangan.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua form input wajib diisi!");
            return;
        }

        java.util.Date dateDariSpinner = (java.util.Date) SpinnerTanggal.getValue();
        java.text.SimpleDateFormat formatMsql = new java.text.SimpleDateFormat("yyyy-MM-dd");

        String tgl = formatMsql.format(dateDariSpinner);
        String jenis = cbJenis_Transaksi.getSelectedItem().toString();
        String kat = cbKategori.getSelectedItem().toString();
        double nom;
        try {
            nom = Double.parseDouble(txtNominal.getText());

            if (nom <= 0) {
                JOptionPane.showMessageDialog(this,
                        "Nominal harus lebih dari 0!");
                return;
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "Nominal harus berupa angka!");
            return;
        }
        String ket = txtKeterangan.getText().trim();

        boolean sukses = control.simpanTransaksi(tgl, jenis, kat, nom, ket);

        if (sukses) {
            JOptionPane.showMessageDialog(this, "Data transaksi berhasil disimpan!");
            refreshTabel();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data ke database.");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void tabelKeuanganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelKeuanganMouseClicked
        
        int baris = tabelKeuangan.getSelectedRow();
        if (baris != -1) {
            idTerpilih = Integer.parseInt(tabelKeuangan.getValueAt(baris, 0).toString());

            try {
                String tglString = tabelKeuangan.getValueAt(baris, 1).toString();
                java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
                java.util.Date dateParsed = sdf.parse(tglString);
                SpinnerTanggal.setValue(dateParsed);
            } catch (Exception e) {
                System.out.println("Gagal set tanggal : " + e.getMessage());
            }

            cbJenis_Transaksi.setSelectedItem(tabelKeuangan.getValueAt(baris, 2).toString());
            cbKategori.setSelectedItem(tabelKeuangan.getValueAt(baris, 3).toString());
            
            String nilaiTabel = tabelKeuangan.getValueAt(baris, 4).toString();
            String nilaiBersih = nilaiTabel.replaceAll("\\D", "");
            txtNominal.setText(nilaiBersih);

            txtKeterangan.setText(tabelKeuangan.getValueAt(baris, 5).toString());
        }
    }//GEN-LAST:event_tabelKeuanganMouseClicked

