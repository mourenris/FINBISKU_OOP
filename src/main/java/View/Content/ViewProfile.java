    private void loadDataKeForm() {
        Model.ModelUser u = controlProfile.getProfile(idUserLogin);
        if (u != null) {
            txtNama.setText(u.getNama());
            txtEmail.setText(u.getEmail());
            Pasword_Field.setText(u.getPassword());
            txtNamaUsaha.setText(u.getNamaUsaha());
            txtAlamat.setText(u.getAlamat());
            txtDeskripsi.setText(u.getDeskripsi()); 
        }
    }

