    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String nama = txtNama.getText().trim();
        String email = txtEmail.getText().trim();
        String pass = new String(Pasword_Field.getPassword()).trim();
        String usaha = txtNamaUsaha.getText().trim();
        String alamat = txtAlamat.getText().trim();
        String desk = txtDeskripsi.getText().trim();

        if (nama.isEmpty() || email.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama, Email, dan Password tidak boleh kosong!");
            return;
        }

        boolean sukses = controlProfile.simpanProfile(idUserLogin, nama, email, pass, usaha, alamat, desk);
        if (sukses) {
            JOptionPane.showMessageDialog(this,
            "Data profil berhasil disimpan!");
            loadDataKeForm();
            setFormEditable(false);
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data profil.");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int konfirmasi = JOptionPane.showConfirmDialog(this, 
                "Apakah Anda yakin ingin menghapus data profil ini?", 
                "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        
        if (konfirmasi == JOptionPane.YES_OPTION) {
            boolean sukses = controlProfile.hapusProfile(idUserLogin);
            if (sukses) {
                JOptionPane.showMessageDialog(this, "Data profil berhasil dihapus!");
                bersihkanForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus profil.");
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed


